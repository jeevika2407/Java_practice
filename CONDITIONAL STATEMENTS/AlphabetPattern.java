package CondiPractice;
public class AlphabetPattern {
	    public static void main(String[] args) {
	        for (char a = 'a', b = 'z'; a <= 'm'; a++, b--) {
	            System.out.print(a + "" + b + " ");
	        }
	        for (char a = 'n', b = 'm'; a <= 'z'; a++, b--) {
	            System.out.print(a + "" + b + " ");
	        }
	    }
	}

